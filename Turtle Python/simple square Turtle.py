import turtle
t1=turtle.Turtle()
def square():
    t1.forward(120)
    t1.right(90)
    t1.forward(120)
    t1.right(90)
    t1.forward(120)
    t1.right(90)
    t1.forward(120)
square()
t1.penup()
t1.forward(120)
t1.pendown()
square()
t1.penup()
t1.forward(120)
t1.pendown()
square()
t1.penup()
t1.forward(120)
t1.pendown()
square()
